package entities;

public class Traingule {

	
	 public double a;
	 public double b;
	 public double c;
	 
	 public double area () {
		 
		 double area = (a + b + c)/2.0 ;
		 double areab = (a + b + c)/2.0 ;
		 double areaf = (area + areab)/2.0;
		 return areaf; 

		 
		 
	 }
	 
	 
	 
	 
	 

}
